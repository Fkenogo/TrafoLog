const mongoose = require('mongoose');

const territorySchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
    trim: true
  },
  code: {
    type: String,
    required: true,
    unique: true,
    uppercase: true,
    trim: true
  },
  description: String,
  region: String,
  is_active: {
    type: Boolean,
    default: true
  }
}, {
  timestamps: {
    createdAt: 'created_at',
    updatedAt: 'updated_at'
  }
});

// Indexes
territorySchema.index({ name: 1 });
territorySchema.index({ code: 1 });

module.exports = mongoose.model('Territory', territorySchema);